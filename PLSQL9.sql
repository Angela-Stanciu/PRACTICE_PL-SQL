--EXEMPLU PRINDERE EXCEPTIE:
DECLARE
    zero number := 0;
    zece number := 10;
    total number;
BEGIN
    total := zece/zero;
EXCEPTION
  WHEN ZERO_DIVIDE THEN
    dbms_output.put_line('Nu merge impartirea la zero.');
  WHEN OTHERS THEN
    dbms_output.put_line('Eroare.');
END;
/
--OBS: MAI SUS ESTE O EXCEPTIE DEFINITA DE SISTEM;


