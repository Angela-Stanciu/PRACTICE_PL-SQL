--EXEMPLU PENTRU ARUNCAREA UNEI EXCEPTII DECLARATE:
DECLARE
    id number := 5;
    exceptie_invalid_id EXCEPTION;
BEGIN
  IF id <= 10 THEN
    RAISE exceptie_invalid_id;
  END IF;
EXCEPTION
  WHEN exceptie_invalid_id THEN
    dbms_output.put_line('Invalid id.');
END;
/
